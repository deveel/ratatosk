namespace Ratatosk
{
    /// <summary>
    /// Defines constant keys for the connection parameters used
    /// to configure the Facebook Messenger connector.
    /// </summary>
    public static class FacebookConnectionParameters
    {
        /// <summary>
        /// The key for the page access token parameter.
        /// </summary>
        public const string PageAccessToken = "PageAccessToken";

        /// <summary>
        /// The key for the page identifier parameter.
        /// </summary>
        public const string PageId = "PageId";

        /// <summary>
        /// The key for the webhook URL parameter.
        /// </summary>
        public const string WebhookUrl = "WebhookUrl";

        /// <summary>
        /// The key for the verify token parameter.
        /// </summary>
        public const string VerifyToken = "VerifyToken";
    }
}
