namespace Ratatosk
{
    /// <summary>
    /// Defines constant keys for the connection parameters used
    /// to configure the Firebase Cloud Messaging connector.
    /// </summary>
    public static class FirebaseConnectionParameters
    {
        /// <summary>
        /// The key for the Firebase project identifier parameter.
        /// </summary>
        public const string ProjectId = "ProjectId";

        /// <summary>
        /// The key for the service account key parameter.
        /// </summary>
        public const string ServiceAccountKey = "ServiceAccountKey";

        /// <summary>
        /// The key for the dry run mode parameter.
        /// </summary>
        public const string DryRun = "DryRun";
    }
}
